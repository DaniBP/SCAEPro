public class NoticiasModel {
	private String tituloNoticia;
	private String descNoticia;
	private String imagenNoticia;
	private int idNoticia;
	private String nombreVentana;
	
	/**
	 * @return the tituloNoticia
	 */
	public String getTituloNoticia() {
		return tituloNoticia;
	}
	/**
	 * @param tituloNoticia the tituloNoticia to set
	 */
	public void setTituloNoticia(String tituloNoticia) {
		this.tituloNoticia = tituloNoticia;
	}
	/**
	 * @return the descNoticia
	 */
	public String getDescNoticia() {
		return descNoticia;
	}
	/**
	 * @param descNoticia the descNoticia to set
	 */
	public void setDescNoticia(String descNoticia) {
		this.descNoticia = descNoticia;
	}
	/**
	 * @return the imagenNoticia
	 */
	public String getImagenNoticia() {
		return imagenNoticia;
	}
	/**
	 * @param imagenNoticia the imagenNoticia to set
	 */
	public void setImagenNoticia(String imagenNoticia) {
		this.imagenNoticia = imagenNoticia;
	}
	/**
	 * @return the idNoticia
	 */
	public int getIdNoticia() {
		return idNoticia;
	}
	/**
	 * @param idNoticia the idNoticia to set
	 */
	public void setIdNoticia(int idNoticia) {
		this.idNoticia = idNoticia;
	}
	/**
	 * @return the nombreVentana
	 */
	public String getNombreVentana() {
		return nombreVentana;
	}
	/**
	 * @param nombreVentana the nombreVentana to set
	 */
	public void setNombreVentana(String nombreVentana) {
		this.nombreVentana = nombreVentana;
	}
}
