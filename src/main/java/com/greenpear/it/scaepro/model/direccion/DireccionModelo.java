package com.greenpear.it.scaepro.model.direccion;

public class DireccionModelo {
	
	private int idDireccion;
	private String estado;
	private String municipio;
	private String colonia;
	private String numIn;
	private String numEx;
	private String calle;
	private String cp;
	private String uniColonia;
	
	public int getIdDireccion() {
		return idDireccion;
	}
	public void setIdDireccion(int idDireccion) {
		this.idDireccion = idDireccion;
	}
	public String getEstado() {
		return estado;
	}
	public void setEstado(String estado) {
		this.estado = estado;
	}
	public String getMunicipio() {
		return municipio;
	}
	public void setMunicipio(String municipio) {
		this.municipio = municipio;
	}
	public String getColonia() {
		return colonia;
	}
	public void setColonia(String colonia) {
		this.colonia = colonia;
	}
	public String getCp() {
		return cp;
	}
	public void setCp(String cp) {
		this.cp = cp;
	}
	public String getNumIn() {
		return numIn;
	}
	public void setNumIn(String numIn) {
		this.numIn = numIn;
	}
	public String getNumEx() {
		return numEx;
	}
	public void setNumEx(String numEx) {
		this.numEx = numEx;
	}
	public String getCalle() {
		return calle;
	}
	public void setCalle(String calle) {
		this.calle = calle;
	}
	public String getUniColonia() {
		return uniColonia;
	}
	public void setUniColonia(String uniColonia) {
		this.uniColonia = uniColonia;
	}
	
}
